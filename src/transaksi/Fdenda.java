    class PremiumDatePicker extends JDialog {
        private Calendar cal = Calendar.getInstance();
        private JPanel pDateGrid;
        private JComboBox<String> cbMonth;
        private JComboBox<Integer> cbYear;
        private boolean isUpdating = false;
        private JTextField targetField;
        private Color mainColor = new Color(0, 102, 204);

        public PremiumDatePicker(JFrame parent, JTextField target) {
            super(parent, "Pilih Tanggal", true);
            this.targetField = target;
            setLayout(new BorderLayout());
            setPreferredSize(new Dimension(380, 420));

            JPanel mainPanel = new JPanel(new BorderLayout());
            mainPanel.setBackground(Color.WHITE);

            JPanel header = new JPanel(new BorderLayout());
            header.setBackground(mainColor);
            header.setPreferredSize(new Dimension(350, 50));
            header.setBorder(new EmptyBorder(5, 10, 5, 10));

            JPanel pnlMonthYear = new JPanel(new FlowLayout(FlowLayout.CENTER, 5, 0));
            pnlMonthYear.setOpaque(false);

            String[] months = { "Januari", "Februari", "Maret", "April", "Mei", "Juni", "Juli", "Agustus", "September",
                    "Oktober", "November", "Desember" };
            cbMonth = new JComboBox<>(months);
            cbMonth.setFont(new Font("Tahoma", Font.BOLD, 12));
            cbMonth.setCursor(new Cursor(Cursor.HAND_CURSOR));

            Integer[] years = new Integer[100];
            int currentYear = Calendar.getInstance().get(Calendar.YEAR);
            for (int i = 0; i < 100; i++) {
                years[i] = currentYear - 50 + i;
            }
            cbYear = new JComboBox<>(years);
            cbYear.setFont(new Font("Tahoma", Font.BOLD, 12));
            cbYear.setCursor(new Cursor(Cursor.HAND_CURSOR));

            pnlMonthYear.add(cbMonth);
            pnlMonthYear.add(cbYear);

            ItemListener comboListener = e -> {
                if (e.getStateChange() == ItemEvent.SELECTED && !isUpdating) {
                    cal.set(Calendar.MONTH, cbMonth.getSelectedIndex());
                    cal.set(Calendar.YEAR, (Integer) cbYear.getSelectedItem());
                    updateGrid();
                }
            };
            cbMonth.addItemListener(comboListener);
            cbYear.addItemListener(comboListener);

            JButton btnPrev = createNavButton("<");
            btnPrev.addActionListener(e -> {
                cal.add(Calendar.MONTH, -1);
                updateGrid();
            });

            JButton btnNext = createNavButton(">");
            btnNext.addActionListener(e -> {
                cal.add(Calendar.MONTH, 1);
                updateGrid();
            });

            header.add(btnPrev, BorderLayout.WEST);
            header.add(pnlMonthYear, BorderLayout.CENTER);
            header.add(btnNext, BorderLayout.EAST);

            JPanel gridContainer = new JPanel(new BorderLayout());
            gridContainer.setBackground(Color.WHITE);
            gridContainer.setBorder(new EmptyBorder(10, 10, 10, 10));

            JPanel pDaysHeader = new JPanel(new GridLayout(1, 7));
            pDaysHeader.setBackground(Color.WHITE);
            String[] days = { "Min", "Sen", "Sel", "Rab", "Kam", "Jum", "Sab" };
            for (String day : days) {
                JLabel l = new JLabel(day, JLabel.CENTER);
                l.setFont(new Font("Tahoma", Font.BOLD, 11));
                l.setForeground(new Color(100, 100, 100));
                pDaysHeader.add(l);
            }
            gridContainer.add(pDaysHeader, BorderLayout.NORTH);

            pDateGrid = new JPanel(new GridLayout(6, 7, 5, 5));
            pDateGrid.setBackground(Color.WHITE);
            gridContainer.add(pDateGrid, BorderLayout.CENTER);

            mainPanel.add(header, BorderLayout.NORTH);
            mainPanel.add(gridContainer, BorderLayout.CENTER);

            JButton btnClose = new JButton("Tutup");
            btnClose.setPreferredSize(new Dimension(0, 35));
            btnClose.addActionListener(e -> dispose());
            mainPanel.add(btnClose, BorderLayout.SOUTH);

            add(mainPanel);
            updateGrid();
            pack();
            setLocationRelativeTo(parent);
        }

        private JButton createNavButton(String text) {
            JButton b = new JButton(text);
            b.setFont(new Font("Tahoma", Font.BOLD, 16));
            b.setForeground(Color.WHITE);
            b.setContentAreaFilled(false);
            b.setBorderPainted(false);
            b.setFocusPainted(false);
            b.setCursor(new Cursor(Cursor.HAND_CURSOR));
            return b;
        }

        private void updateGrid() {
            pDateGrid.removeAll();

            isUpdating = true;
            cbMonth.setSelectedIndex(cal.get(Calendar.MONTH));
            cbYear.setSelectedItem(cal.get(Calendar.YEAR));
            isUpdating = false;

            Calendar temp = (Calendar) cal.clone();
            temp.set(Calendar.DAY_OF_MONTH, 1);
            int startDay = temp.get(Calendar.DAY_OF_WEEK) - 1;
            int maxDays = temp.getActualMaximum(Calendar.DAY_OF_MONTH);

            for (int i = 0; i < startDay; i++) {
                pDateGrid.add(new JLabel(""));
            }

            for (int day = 1; day <= maxDays; day++) {
                final int selectedDay = day;
                JButton b = new JButton(String.valueOf(day));
                b.setFont(new Font("Tahoma", Font.PLAIN, 12));
                b.setFocusPainted(false);
                b.setMargin(new Insets(0, 0, 0, 0));
                b.setBackground(new Color(250, 250, 250));
                b.setCursor(new Cursor(Cursor.HAND_CURSOR));

                b.addActionListener(e -> {
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    cal.set(Calendar.DAY_OF_MONTH, selectedDay);
                    targetField.setText(sdf.format(cal.getTime()));
                    dispose();
                });
                pDateGrid.add(b);
            }

            int usedSlots = startDay + maxDays;
            for (int i = usedSlots; i < 42; i++) {
                pDateGrid.add(new JLabel(""));
            }

            pDateGrid.revalidate();
            pDateGrid.repaint();
        }
    }
